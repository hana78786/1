package gugudan;
public class gugudan5 {
	public static void main(String[] args) {
		System.out.println("6단");
		int i=1;
		while(i<10){ 
			System.out.println(6*i); //6Xi의 값을 출력한다
			i=i+1; //i의 값을 i+1으로 다시 변경한다

	}
		System.out.println("7단");
		for(int c = 1;c<10;c++) {
			System.out.println(7*c);
		}
	} 

}