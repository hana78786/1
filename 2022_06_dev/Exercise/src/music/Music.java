package music;

public class Music {
	private String SongTitle;
	private String Singer;
	
	
	public String getSongTitle() {
		return SongTitle;
	}
	public void setSongTitle(String songTitle) {
		SongTitle = songTitle;
	}
	public String getSinger() {
		return Singer;
	}
	public void setSinger(String singer) {
		Singer = singer;
	}
	
	

}
