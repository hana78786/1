package com.kh.variable;

public class TestVariable {
	public static void main(String[] args) {
		int kh; // 변수의 선언
		int num1 = 1;
		int num2 = 2;
		kh = 1; // 변수의 초기화 값을 대입함
		System.out.println(kh + 1);
		kh = 2; // 변수의 초기화 재 설정
		System.out.println(kh + 1);
		System.out.println(num1 + 1);
		System.out.println(num2 + 1);
	}

}
